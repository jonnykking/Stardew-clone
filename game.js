// --- Audio System ---
const AudioContext = window.AudioContext || window.webkitAudioContext;
const audioCtx = new AudioContext();

function playTone(freq, type, duration, vol) {
    if (audioCtx.state === 'suspended') audioCtx.resume();
    const osc = audioCtx.createOscillator();
    const gainNode = audioCtx.createGain();

    osc.type = type;
    osc.frequency.setValueAtTime(freq, audioCtx.currentTime);

    // Quick attack, exponential decay
    gainNode.gain.setValueAtTime(vol, audioCtx.currentTime);
    gainNode.gain.exponentialRampToValueAtTime(0.01, audioCtx.currentTime + duration);

    osc.connect(gainNode);
    gainNode.connect(audioCtx.destination);

    osc.start();
    osc.stop(audioCtx.currentTime + duration);
}

function sfxThwack() {
    playTone(150, 'triangle', 0.1, 0.5);
    setTimeout(() => playTone(100, 'square', 0.15, 0.5), 20);
}

function sfxClink() {
    playTone(800, 'sine', 0.05, 0.3);
    setTimeout(() => playTone(1200, 'sine', 0.1, 0.2), 30);
}

function sfxSplash() {
    playTone(300, 'sine', 0.1, 0.3);
    setTimeout(() => playTone(200, 'triangle', 0.2, 0.2), 50);
}

function sfxPop() {
    playTone(600, 'sine', 0.05, 0.4);
    setTimeout(() => playTone(800, 'sine', 0.1, 0.4), 20);
}

function sfxError() {
    playTone(150, 'sawtooth', 0.2, 0.3);
}

