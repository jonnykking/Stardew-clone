// --- Map Generation ---
let map = [];
function generateMap() {
    for (let y = 0; y < MAP_HEIGHT; y++) {
        let row = [];
        for (let x = 0; x < MAP_WIDTH; x++) {
            // Borders are water
            if (x === 0 || y === 0 || x === MAP_WIDTH - 1 || y === MAP_HEIGHT - 1) {
                row.push(TILES.WATER);
                continue;
            }

            // Random distribution for a farm area
            let rand = Math.random();
            if (rand < 0.02) {
                row.push(TILES.WATER);
            } else if (rand < 0.15) {
                row.push(TILES.TREE);
            } else if (rand < 0.20) {
                row.push(TILES.ROCK);
            } else {
                row.push(TILES.GRASS);
            }
        }
        map.push(row);
    }

    // Ensure player start area (center) is clear
    let cx = Math.floor(MAP_WIDTH / 2);
    let cy = Math.floor(MAP_HEIGHT / 2);
    for (let i = -2; i <= 2; i++) {
        for (let j = -2; j <= 2; j++) {
            map[cy + j][cx + i] = TILES.GRASS;
        }
    }
    // Place a little bed/tent house nearby
    map[cy - 2][cx] = TILES.HOUSE;

    // Place a shipping bin to sell goods
    map[cy][cx - 2] = TILES.BIN;
}
generateMap();

